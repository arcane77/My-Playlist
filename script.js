var song1 = document.getElementById("song1");
var icon0 = document.getElementById("icon0");
icon0.onclick = function(){
    if(song1.paused){
        song1.play();
        icon0.src = "icons8-pause-100.png";
        
    }else{
        song1.pause();
        icon0.src = "icons8-play-100.png";
        
       
    }
}

